function scitani(a,b){
    return a + b;
}

function odcitani(a,b){
    return a - b;
}

function nasobeni(a,b){
    return a * b;
}

function deleni(a,b){
    return a / b;
}

function sin(a){
    return Math.sin(a);
}
function cos(a){
    return Math.cos(a);
}
function tg(a){
    return Math.tan(a);
}
function cotg(a){
    return Math.cotg(a);
}
function Log(a){
    return Math.log(a);
}
function exponent(a,b){
    return a ** b;
}
function Odmocnina(a){
    return Math.sqrt(a);
}


